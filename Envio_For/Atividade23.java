public class Atividade23 {
    public static void main(String[] args) {
        // Loop de 1 a 200
        for (int i = 1; i <= 200; i++) {
            // Verifica se o número é divisível por 4
            if (i % 4 == 0) {
                System.out.println(i); // Exibe o número divisível por 4
            }
        }
    }
}
