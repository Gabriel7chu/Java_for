public class Atividade11{
    public static void main(String[] args) {
        // Percorrer os números de 1 a 10
        for (int i = 1; i <= 10; i++) {
            // Calcular o quadrado do número
            int quadrado = i * i;
            // Exibir o número e seu quadrado
            System.out.println("O quadrado de " + i + " é: " + quadrado);
        }
    }
}