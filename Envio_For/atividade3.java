
public class atividade3
{
    public static void main(String[] args) {
        
        // Laço de repetição 'for' que começa com 'n' igual a 2, 
        // e vai incrementando de 2 em 2 até atingir o valor 20 (inclusive)
        for (int n = 2; n <= 20; n = n + 2) {
            
            // Aqui estamos imprimindo o valor de 'n' em cada iteração do laço
            System.out.printf("O valor de n é: %d\n", n);
        }
    }
}