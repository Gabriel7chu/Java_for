
public class atividade1
{
    public static void main(String[] args) {
        
        // Laço de repetição 'for' que começa com 'n' igual a 0, 
        // e vai incrementando de 1 em 1 até atingir o valor 10 (inclusive)
        for (int n = 0; n <= 10; n = n + 1) {
            
            // Aqui estamos imprimindo o valor de 'n' em cada iteração do laço
            System.out.printf("O valor de n é: %d\n", n);
        }
    }
}
