public class Atividade5 {
    public static void main(String[] args) {
        int soma = 0;

        // Loop para somar os números de 1 a 100
        for (int i = 1; i <= 100; i++) {
            soma += i;
        }

        // Exibe o resultado
        System.out.println("A soma dos números de 1 a 100 é: " + soma);
    }
}